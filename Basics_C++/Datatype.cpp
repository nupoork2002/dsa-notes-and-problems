// 2.DataTypes 

#include <iostream>
using namespace std;

int main() {
    int a = 10;
    float b = 5.5;
    char c = 'A';
    bool d = true;
    double e = 9.87654321;
    
    cout << "Integer: " << a << endl;
    cout << "Float: " << b << endl;
    cout << "Character: " << c << endl;
    cout << "Boolean: " << d << endl;
    cout << "Double: " << e << endl;
    
    return 0;
}
